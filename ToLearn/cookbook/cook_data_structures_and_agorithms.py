# 01. Unpacking a Sequence into Separate Variables
# Unpacking actually works with any object that happens to be iterable
data = [ 'ACME', 50, 91.1, (2012, 12, 21) ]
_, shares, price, _ = data = data
print('shares: ',shares)
print('price: ',price)










